
function exercicio_m2_05(){

    let number_01;
    let number_02;

    let soma;
    let sub;
    let mult;
    let div;


    number_01 = Number(prompt("Informe um número"));
    number_02 = Number(prompt("Informe outro número"));

    soma = number_01 + number_02;
    sub = number_01 - number_02;
    mult = number_01 * number_02;
    div = number_01 / number_02;

    console.log("Soma: " + soma);
    console.log("Subtração: " + sub);
    console.log("Multiplicação: " + mult);
    console.log("Divisão: " + div);

};

